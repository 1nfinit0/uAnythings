    private void tbnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbnAgregarActionPerformed
        
        String estudiante = txtEstudiante.getText();
        String profesion = txtProfesion.getText();

        if (!estudiante.isEmpty() && !profesion.isEmpty()) {
            profesiones.put(estudiante, profesion);
            JOptionPane.showMessageDialog(null, "Profesión agregada correctamente.");
            txtEstudiante.setText("");
            txtProfesion.setText("");
        }       
    }//GEN-LAST:event_tbnAgregarActionPerformed

