    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String estudiante = txtEstudiante.getText();
        if (profesiones.containsKey(estudiante)) {
            textArea.setText("La profesión de " + estudiante + " es: " + profesiones.get(estudiante));
        } else {
            textArea.setText("No se encontró la profesión para " + estudiante);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        StringBuilder sb = new StringBuilder();
        for (String estudiante : profesiones.keySet()) {
            sb.append(estudiante).append("-> ").append(profesiones.get(estudiante)).append("\n");
        }
        textArea.setText(sb.toString());
    }//GEN-LAST:event_btnMostrarActionPerformed

